'use client';

import { useState } from 'react';

// Hook for managing mobile sidebar state
export function useMobileNav() {
  const [isOpen, setIsOpen] = useState(false);

  return {
    isOpen,
    open: () => setIsOpen(true),
    close: () => setIsOpen(false),
    toggle: () => setIsOpen((prev) => !prev),
  };
}
